require 'test_helper'

class TransfersControllerTest < ActionController::TestCase
  setup do
    @transfer = transfers(:one)
  end

  test "should get index" do
    get :index
    assert_response :success
    assert_not_nil assigns(:transfers)
  end

  test "should get new" do
    get :new
    assert_response :success
  end

  test "should create transfer" do
    assert_difference('Transfer.count') do
      post :create, transfer: { airport_id: @transfer.airport_id, arriving_at: @transfer.arriving_at, crash_at: @transfer.crash_at, day: @transfer.day, voyage_id: @transfer.voyage_id }
    end

    assert_redirected_to transfer_path(assigns(:transfer))
  end

  test "should show transfer" do
    get :show, id: @transfer
    assert_response :success
  end

  test "should get edit" do
    get :edit, id: @transfer
    assert_response :success
  end

  test "should update transfer" do
    put :update, id: @transfer, transfer: { airport_id: @transfer.airport_id, arriving_at: @transfer.arriving_at, crash_at: @transfer.crash_at, day: @transfer.day, voyage_id: @transfer.voyage_id }
    assert_redirected_to transfer_path(assigns(:transfer))
  end

  test "should destroy transfer" do
    assert_difference('Transfer.count', -1) do
      delete :destroy, id: @transfer
    end

    assert_redirected_to transfers_path
  end
end
